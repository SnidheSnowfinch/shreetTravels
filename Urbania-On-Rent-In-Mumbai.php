          <p>Chor Bazaar, also known as the 'Thieves Market,' is one of Mumbai’s most famous street markets, known for
            its eclectic offerings. The market is a treasure trove for antique lovers, where you can find everything
            from vintage furniture and rare collectibles to old Bollywood posters and jewelry. The vibrant, chaotic
            atmosphere of the market adds to its charm, making it a fascinating place to explore. Whether you’re on the
            hunt for unique souvenirs or just curious about the local culture, Chor Bazaar is a must-visit for those
            seeking a taste of Mumbai's bustling markets.</p>
        </div>
        <div class="mt-3">
