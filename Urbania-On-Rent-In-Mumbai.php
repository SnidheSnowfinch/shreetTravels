          <p>A UNESCO World Heritage site, Chhatrapati Shivaji Maharaj Terminus (CST) is an architectural gem in Mumbai,
            combining Victorian Gothic and traditional Indian architectural styles. Built in 1887, CST serves as one of
            India's busiest railway stations and is known for its grandeur and intricacy. The station’s dome, spires,
            and detailed carvings make it a visual masterpiece, while its historical significance as a major railway hub
            adds to its allure. A visit to CST allows you to witness the beauty of British-era architecture fused with
            Indian design elements.</p>

