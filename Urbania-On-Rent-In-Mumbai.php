          <p>Juhu Beach is one of Mumbai's most popular beaches, known for its lively atmosphere and scenic beauty. The
            beach is an ideal spot for a relaxing day by the sea, offering visitors the chance to enjoy local snacks,
            take a leisurely walk, or simply soak in the view of the Arabian Sea. Families, friends, and tourists flock
            here to watch the sunset, participate in beach activities, and enjoy the cool sea breeze. Juhu Beach is also
            home to several famous Bollywood celebrities, making it a hotspot for those wanting to catch a glimpse of
            the stars.</p>
        </div>
