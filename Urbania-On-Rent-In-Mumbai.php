          <p>One of the most famous and revered temples in Mumbai, Siddhivinayak Temple is dedicated to Lord Ganesha,
            the remover of obstacles. Located in the Prabhadevi area, the temple is a major spiritual center, drawing
            thousands of devotees every day. The temple’s beautifully carved architecture, along with its serene
            ambiance, offers a peaceful retreat for visitors. The Siddhivinayak Temple holds immense significance, and
            it is believed that praying here can bring good fortune and success.</p>
        </div>
        <div class="mt-3">
          <h5>Haji Ali Dargah</h5>
          <p>Located on a small islet in the Arabian Sea, Haji Ali Dargah is both a mosque and a tomb, dedicated to the
            Muslim saint Haji Ali. The dargah is a symbol of Mumbai’s spiritual diversity, attracting both devotees and
            tourists from around the world. A causeway leads visitors to the shrine, where they can enjoy stunning views
            of the sea and the city. The peaceful atmosphere, along with the historic significance of the shrine, makes
            it an essential spiritual destination for anyone visiting Mumbai.</p>
        </div>
        <div class="mt-3">
