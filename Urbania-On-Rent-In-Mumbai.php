            fashion, antiques, and jewelry to quirky souvenirs. The energetic atmosphere, combined with colorful stalls,
            makes it the perfect place to experience Mumbai’s lively street culture. Whether you're hunting for vintage
            items or simply soaking in the city’s energetic vibe, Colaba Causeway has something for everyone.</p>
        </div>
        <div class="mt-3">
          <h5>Elephanta Caves</h5>
          <p>Located on Elephanta Island, just a short ferry ride from the city, the Elephanta Caves are ancient
            rock-cut temples dedicated to Lord Shiva. These caves are famous for their intricate sculptures and reliefs,
            which depict scenes from Hindu mythology. The main cave, known as the ‘Cave of Shiva,’ houses a massive
            sculpture of Trimurti, depicting the three faces of Lord Shiva. The boat ride to Elephanta Island, coupled
            with the exploration of the caves, offers a glimpse into Mumbai's rich historical and religious heritage.
          </p>
        </div>
        <div class="mt-3">
